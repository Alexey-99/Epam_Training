	private static Lexeme createLexeme(List<Symbol> symbolsList) {
		return new Lexeme(symbolsList);
	}

	private static Sentence createSentence(List<Lexeme> lexemes) {
		return new Sentence(lexemes);
	}

	private static Paragraph createParagraph(List<Sentence> sentences) {
		return new Paragraph(sentences);
	}

	private static Text createText(List<Paragraph> paragraphs) {
		return new Text(paragraphs);
	}

	private static List<Symbol> createListSymbols(Character... symbols) {
		List<Symbol> symbolsList = new ArrayList<>();
		Arrays.stream(symbols).forEach(symbol -> symbolsList.add(new Symbol(symbol)));
		return symbolsList;
	}

	private static List<Lexeme> createListLexemes(Lexeme... lexemes) {
		List<Lexeme> lexemeList = new ArrayList<>();
		Arrays.stream(lexemes).forEach(lexeme -> lexemeList.add(lexeme));
		return lexemeList;
	}

	private static List<Sentence> createListSentences(Sentence... sentences) {
		List<Sentence> sentenceList = new ArrayList<>();
		Arrays.stream(sentences).forEach(sentence -> sentenceList.add(sentence));
		return sentenceList;
	}

	private static List<Paragraph> createListParagraphs(Paragraph... paragraphs) {
		List<Paragraph> paragraphList = new ArrayList<>();
		Arrays.stream(paragraphs).forEach(paragraph -> paragraphList.add(paragraph));
		return paragraphList;
	}
